def random_seed(h,w)
  out = []
  h.times do
    row = []
    w.times do
      row << rand(2)
    end
    out << row
  end

  return out
end

def print_board(generation, board)
  height = board.length - 1
  width = board[0].length - 1

  buffer = ""

  buffer << "\033[;H"
  buffer << "Generation #{generation}\n"
  buffer << " "
  (0..width).each do |w|
    buffer << "#{w % 10} "
  end
  buffer << "\n"
  (0..height).each do |h|
    buffer <<  "#{h % 10} "
    (0..width).each do |w|
      buffer << (board[h][w] == 1 ? "x " : "  ")
    end
    buffer <<  "\n"
  end
  print buffer
end

def count_live_neighbors(board, h, w)
  count = 0
  height = board.length - 1
  width = board[0].length - 1
  (-1..1).each do |relative_h|
    (-1..1).each do |relative_w|
      if [relative_w, relative_h] != [0,0] # ignore ourselves
        h2 = h + relative_h
        w2 = w + relative_w
        if (0..height).include?(h2) && (0..width).include?(w2)
         #  puts "found valid neighbor #{h2}, #{w2} (#{board[h2][w2]})"
          if board[h2][w2] == 1
            count += 1
          end
        end
      end
    end
  end

  return count
end

def next_board(board)
  new_board = Marshal.load(Marshal.dump board)
  height = board.length - 1
  width = board[0].length - 1
  (0..height).each do |h|
    (0..width).each do |w|
      live_neighbor_count = count_live_neighbors(board, h, w)
      if board[h][w] == 1 # we are alive
        if live_neighbor_count < 2
          # Any live cell with fewer than two live neighbours dies, as if by underpopulation.
          new_board[h][w] = 0
        elsif live_neighbor_count == 2 || live_neighbor_count == 3
          # Any live cell with two or three live neighbours lives on to the next generation.
          new_board[h][w] = 1
        else
          # Any live cell with more than three live neighbours dies, as if by overpopulation.
          new_board[h][w] = 0
        end
      else # we are dead
        # Any dead cell with exactly three live neighbours becomes a live cell, as if by reproduction.
        new_board[h][w] = (live_neighbor_count == 3 ? 1 : 0)
      end
    end
  end

  return new_board
end

def conway(board)
  system('clear')
  generation = 0
  #system('clear')
  print_board(generation, board)
  sleep 0.1
  while true do
    #system('clear')
    board = next_board(board)
    generation += 1
    print_board(generation, board)
    sleep 0.1
  end
end

